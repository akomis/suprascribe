export function BadgesCarousel() {
  if (BADGES.length === 0) return null

  // Single badge — no carousel needed
  if (BADGES.length === 1) {
    const b = BADGES[0]
    return (
      <div className="flex justify-center pt-6">
        <a href={b.href} target="_blank" rel="noopener noreferrer">
          <img src={b.imgSrc} alt={b.alt} width={b.width} height={b.height} loading="lazy" />
        </a>
      </div>
    )
  }

  // Duplicate for seamless loop
  const track = [...BADGES, ...BADGES]

  const duration = `${Math.max(30, BADGES.length * 1.5)}s`

  return (
    <div className="relative w-full overflow-hidden pt-6 mask-[linear-gradient(to_right,transparent,black_10%,black_90%,transparent)]">
      <div
        className="flex gap-6 w-max"
        style={{ animation: `badge-scroll ${duration} linear infinite`, willChange: 'transform' }}
      >
        {track.map((b, i) => (
          <a
            key={i}
            href={b.href}
            target="_blank"
            rel="noopener noreferrer"
            className="shrink-0 flex items-center"
          >
            <img
              src={b.imgSrc}
              alt={b.alt}
              width={b.width}
              height={b.height}
              loading="lazy"
              style={{ height: '50px', width: 'auto' }}
            />
          </a>
        ))}
      </div>
    </div>
  )
}
